package com.capg.corejava.basics;

public class ArithmeticOperators 
{
	public static void main(String[] args) 
	{
       int a = 20;
       int b = 10;
       
       int res = a+b;
       System.out.println("a+b = "+ res);
       
       res = a-b;
       System.out.println("a-b = "+ res);
       
       res = a*b;
       System.out.println("a*b = "+ res);
       
       res = a/b;
       System.out.println("a/b = "+ res);
       
       res = a%b;
       System.out.println("a%b = "+ res);
       
	}
}
