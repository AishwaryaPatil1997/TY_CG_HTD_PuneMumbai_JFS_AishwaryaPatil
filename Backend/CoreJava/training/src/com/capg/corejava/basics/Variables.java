package com.capg.corejava.basics;

public class Variables 
{
	public static void main(String[] args) 
	{
        byte b = 10;            //Declaration and initialization of byte data type
        short s = 101;          //Declaration and initialization of short data type
        int i = 1101;           //Declaration and initialization of integer data type
        long l = 11101l;        //Declaration and initialization of long data type
        float f = 101.11f;      //Declaration and initialization of float data type
        double d = 11100011.11; //Declaration and initialization of double data type
        char c = 'A';           //Declaration and initialization of char data type
        boolean q = true;       //Declaration and initialization of boolean data type
        
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(q);
	}
}
