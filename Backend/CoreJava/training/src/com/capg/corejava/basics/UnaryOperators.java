package com.capg.corejava.basics;

public class UnaryOperators 
{
	public static void main(String[] args) 
	{
       int a = 20;
       int b = 10;
       
       System.out.println(a++);
       System.out.println(++a);
       System.out.println(a--+b);
       System.out.println(--a);
       System.out.println(b++-a);
	}
}
