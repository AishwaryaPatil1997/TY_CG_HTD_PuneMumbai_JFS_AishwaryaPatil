package com.capg.corejava.basics;

public class AssignmentOperators 
{
	public static void main(String[] args) 
	{
		int a = 20;
		int b = 10;

		a += b;
		System.out.println("Value of a is " + a);

		a -= b;
		System.out.println("Value of a is " + a);

		a *= b;
		System.out.println("Value of a is " + a);

		a /= b;
		System.out.println("Value of a is " + a);

		a %= b;
		System.out.println("Value of a is " + a);
	}
}
